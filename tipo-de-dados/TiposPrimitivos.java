public class TiposPrimitivos{
        public static void main(String[] args) {

            // Tipos primitivos em Java
            
            int numeroInteiro = 10;          // Tipo inteiro 
            double numeroDecimal = 10.5;     // Tipo ponto flutuante 
            char caractere = 'A';            // Tipo caractere que é UMA SÓ LETRA e com aspas simples
            boolean verdadeiroFalso = true;  // Tipo booleano

            // Exibindo os valores
            System.out.println("Número Inteiro: " + numeroInteiro);
            System.out.println("Número Decimal: " + numeroDecimal);
            System.out.println("Caractere: " + caractere);
            System.out.println("Booleano: " + verdadeiroFalso);
        }   
} 